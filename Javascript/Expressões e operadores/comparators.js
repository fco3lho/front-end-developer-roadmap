/*
  Os operadores de comparação irão comparar valores e retornar 
  um booleano como resposta.

  Os operadores de comparação são separados como:
    * == : igual a
    * != : diferente de
    * > : maior que
    * < : menor que
    * >= maior ou igual que
    * <= menor ou igual que
    * === : estritamente igual a
    * !== : estritamente diferente de
*/

console.log(1 == 1)
console.log(1 == 2)

console.log(1 === "1")
console.log(1 === 1)