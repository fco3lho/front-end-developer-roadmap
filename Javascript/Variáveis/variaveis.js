/*
As variáveis são caracterizadas por nomes simbólicos para receberem algum valor.

Existem 3 palavras reservadas para criar uma variável, são elas: var, let e const.

*/

var nome = "Felipe"
console.log("Nome: " + nome)
console.log("Tipo da variável nome: " + typeof nome)

let idade = 19
console.log("Idade: " + idade)
console.log("Tipo da variável idade: " + typeof idade)

const genero = "Masculino" //A "variável" const após receber algum valor não pode ser alterada devido a ela ser constante.
console.log("Gênero: " + genero)
console.log("Tipo da variável genero: " + typeof genero)

