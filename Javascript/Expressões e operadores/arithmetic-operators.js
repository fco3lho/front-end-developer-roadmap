//Operadores aritméticos

//multiplicação
console.log(1.5 * 15)

//divisão
console.log(10 / 2.5)

//soma
console.log(10 + 10)

//subtração
console.log(20 - 10)

//resto da divisão
console.log(10 % 3)

//incremento
let incremento = 0
incremento++ //soma 1
console.log(incremento)

//decremento
let decremento = 10
decremento-- //subtrai 1
console.log(decremento)

//exponencial
console.log(5 ** 3)