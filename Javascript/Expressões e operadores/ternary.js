/*
  Operador condicional ternário, dependendo da condição, receberemos 
  valores diferentes, exemplo, "Se 1 > 0, retorne true, se não, retorne false".
*/

let x = 10
let y = 9

if(x == 10){
  console.log(true)
}else{
  console.log(false)
}

if(y == 10){
  console.log(true)
}else{
  console.log(false)
}

if(x == 10 && y == 10){
  console.log(true)
}else{
  console.log(false)
}
